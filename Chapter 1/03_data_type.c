#include <stdio.h>

int main()
{
    // int a;
    // a = 1;    //this both are work as same
    int a = 1;

    // float b = 1.4;
    float b; // this both are work as same
    b = 1.4;

    // char c = 'a';
    char c;  // this both are work as same
    c = 'a'; // and in char function use single quotes('')

    printf("The value of a is %d\n", a); // %d is used for int values
    printf("The value of b is %f\n", b); // %f is used for float values
    printf("The value of c is %c\n", c); // %c is used for char values

    // this all are main primary datatype
}