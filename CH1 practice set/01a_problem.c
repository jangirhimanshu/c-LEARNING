#include<stdio.h>

int main()
{
    int length=4;
    int width=5;
    printf("the area of rectangle is\n%d",length*width);

    float _length=4.5;
    float _width=5.5;                                          // when using float values use different variables as int for ex=(_length,_width) 
    printf("\nthe area of rectangle is\n%f",_length*_width);


    return 0;
}